// Null is basically a variable which has value assigned but that value is empty or nothing you can say.
let variable1=null;
console.log("value of variable1 is: ",variable1); // it will print null means empty 

// undefined is basically a variable is declared but not defined yet (means not assigned any value yet)
let variable2;
console.log("value of variable2 is: ",variable2); // it will print undefined

