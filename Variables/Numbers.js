// In number , all types of numbers are represented  like integer ,float , hexadecimal ,octal ,binary etc

let a =10;
let b =23.5;
let ans;

// we can add numbers 
ans=a+b;
console.log("Addition is: ",ans);

// we can subtract numbers 
ans=b-a;
console.log("Subtraction is: ",ans);

// we can multiply numbers
ans=a*b;
console.log("Multiplication is: ",ans);

// we can divide numbers
ans=b/a;
console.log("Division is:",ans);

// we can find modulus of numbers
ans=b%a;
console.log("Modulus is: ",ans);

