// String is datatype which can be used to define text data

let name = "John"; // using double quotes
let city = 'New York'; // using single quotes

// just use either of them but be consistent

console.log("value of name is: ", name);
console.log("value of city is: ", city);    

// Indexing 
// In programming index starts from 0
console.log("character at index 0 is: ", name[0]); // it will print J
console.log("character at index 1 is: ", name[1]);// it will print o
console.log("character at index 2 is: ", name[2]); // it will print h
console.log("character at index 3 is: ", name[3]); // it will print n   

