// we can define multiple variables in same line
let var1 = 10, var2 = 20, var3 = 30;
console.log("value of var1 is: ", var1);

console.log("value of var2 is: ", var2);

console.log("value of var3 is: ", var3);    

// we can also define multiple variables in same line without assigning any value
let a, b, c;
console.log("value of a is: ", a); // it will print undefined   

console.log("value of b is: ", b); // it will print undefined

console.log("value of c is: ", c); // it will print undefined

