// Includes

// checking whether a value exists in an array is really
// easy in javascript , just use the .includes() method.

const fruits=["banana","apple","jackfruit","orange","watermelon"];
console.log(fruits);

console.log(fruits.includes("banana"));//true
console.log(fruits.includes("mango"));//false

// If the elements which you are searching is in the array it will return true else false.