// Throwing Errors

/*
*   Sometimes errors are thrown implicitly by the Javascript runtime,
*   as we saw in the past examples. But sometimes we need to throw errors
*   ourselves explicitly. The throw statements is how we do it:
*
*      throw new Error("Something went wrong");
*
*   It's worth mentioning that javascript will allow you to throw anything you
*   want , not just error objects:
*
*       throw "Something went wrong";
*
*   But for consistency and maintainability, I recommend always throwing error objects.
* 
* */

